# grading
# ---------------------------------------------------------------------------

def grade_quiz(task: Task, responses: List[Dict]) -> dict:
    """Grade a quiz attempt.

    responses: `[{'qid': <index>, 'selected': '<A|B|C|D>'}, ...]`.
    Returns a result dict:
        {
            correct, total, pct, threshold, passed,
            details: [{qid, question, options, correct_answer,
                       selected, is_correct, explanation}, ...]
        }
    """
    questions = task.quiz_questions or []
    by_qid = {r.get('qid'): (r.get('selected') or '').strip().upper()[:1]
              for r in responses if isinstance(r, dict)}

    correct_count = 0
    details = []
    for idx, q in enumerate(questions):
        correct_key = (q.get('correct_answer') or '').strip().upper()[:1]
        selected = by_qid.get(idx, '')
        is_correct = bool(correct_key) and selected == correct_key
        if is_correct:
            correct_count += 1
        details.append({
            'qid': idx,
            'question': q.get('question', ''),
            'options': q.get('options', []),
            'correct_answer': correct_key,
            'selected': selected,
            'is_correct': is_correct,
            'explanation': q.get('explanation', ''),
        })

    total = len(questions)
    pct = int(round(100 * correct_count / total)) if total else 0
    threshold = task.pass_threshold_pct()
    return {
        'correct': correct_count,
        'total': total,
        'pct': pct,
        'threshold': threshold,
        'passed': pct >= threshold,
        'details': details,
    }
